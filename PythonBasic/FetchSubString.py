emailAddress = "turker.yasin.yazkan@huawei.com"
# Fetch value from any index, just  by passing index
print(emailAddress[7])  # index starts from 0

# Fetch substring by starting and end index
print(emailAddress[0:7])  # start index is included but end index is excluded

# Fetch substring by giving starting index only ( from 6 to last)
print(emailAddress[6:])

# Fetch substring by giving end index only ( from first to 6 )
print(emailAddress[:6])



